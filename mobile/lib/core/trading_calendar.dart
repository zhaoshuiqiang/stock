/// A股交易日历：法定节假日集合
/// 用于 tradingDaysBetween 等交易日计算场景，避免长假期间里程碑提前触发。
/// 维护规则：每年12月证监会公布次年休市安排后更新。
class TradingCalendar {
  TradingCalendar._();

  /// 判断某天是否为非交易日（周末 + 法定节假日）
  static bool isNonTradingDay(DateTime date) {
    final weekday = date.weekday;
    if (weekday == DateTime.saturday || weekday == DateTime.sunday) return true;
    final key =
        '${date.year}-${date.month.toString().padLeft(2, '0')}-${date.day.toString().padLeft(2, '0')}';
    return _holidays.contains(key);
  }

  /// 判断某天是否为交易日
  static bool isTradingDay(DateTime date) => !isNonTradingDay(date);

  /// 2025年A股法定节假日（证监会公告）
  /// 2026年A股法定节假日（预估，实际以证监会公告为准）
  static const Set<String> _holidays = {
    // ── 2025 ──
    // 元旦
    '2025-01-01',
    // 春节
    '2025-01-28', '2025-01-29', '2025-01-30', '2025-01-31',
    '2025-02-03', '2025-02-04',
    // 清明
    '2025-04-04',
    // 劳动节
    '2025-05-01', '2025-05-02', '2025-05-05',
    // 端午
    '2025-05-31', '2025-06-02',
    // 中秋+国庆
    '2025-10-01', '2025-10-02', '2025-10-03',
    '2025-10-06', '2025-10-07', '2025-10-08',

    // ── 2026 ──
    // 元旦
    '2026-01-01', '2026-01-02',
    // 春节（预估：1月底-2月初）
    '2026-02-16', '2026-02-17', '2026-02-18', '2026-02-19',
    '2026-02-20', '2026-02-23',
    // 清明
    '2026-04-06',
    // 劳动节
    '2026-05-01', '2026-05-04', '2026-05-05',
    // 端午
    '2026-06-19',
    // 中秋
    '2026-09-25',
    // 国庆
    '2026-10-01', '2026-10-02', '2026-10-05',
    '2026-10-06', '2026-10-07', '2026-10-08',
  };
}
